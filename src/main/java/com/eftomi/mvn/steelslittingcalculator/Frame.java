    private void notesAndWarningsDisplay() {
        notesAndWarnings[1] = "";
        notesAndWarnings[2] = "";
        if (wasteEdgeWidth > edgeMax) {
            notesAndWarnings[1] += moreEdgeMessage;
        }
        if (widthOfRaw - 2 * edgeMin < sumSumWidth) {
            notesAndWarnings[1] += shortEdgeMessage;
        }
        if (se.isNoCoilWidth()) {
            notesAndWarnings[2] = noEdgeMessage;
        }
    }

